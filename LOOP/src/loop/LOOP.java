
package loop;


public class LOOP {

    
    public static void main(String[] args) {
        int[]age={1,2,3,4,5};
        for(int k:age){
            switch(k){
                case 1:
                    System.out.println("firstly");
                    break;
                case 2:
                    System.out.println("secondly");
                    break;
              // default:
                //    System.out.println("default");
            }
        }
    }
    
}
